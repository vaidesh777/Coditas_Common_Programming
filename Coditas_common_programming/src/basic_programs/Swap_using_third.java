//PROGRAM FOR SWAPPING WITHOUT USING THIRD VARIABLE

package basic_programs;

public class Swap_using_third {

	public static void main(String[] args) {
		int x=4,y=5;
		System.out.println("number before swapping"+"\nx="+x+"\ny="+y);
		x=x+y;
		y=x-y;
		x=x-y;
		System.out.println("numbers after swapping"+"\nx="+x+"\ny="+y);
		
		
		
		
		
		
		
		
		
		
	}

}
